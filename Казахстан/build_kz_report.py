def shade(cell, hexcolor):
    tcPr = cell._tc.get_or_add_tcPr()
    sh = OxmlElement('w:shd'); sh.set(qn('w:val'), 'clear')
    sh.set(qn('w:color'), 'auto'); sh.set(qn('w:fill'), hexcolor)
    tcPr.append(sh)

def set_cell(cell, text, bold=False, color=None, size=9.5, align='left', white=False):
    cell.text = ''
    p = cell.paragraphs[0]
    p.alignment = {'left':WD_ALIGN_PARAGRAPH.LEFT,'center':WD_ALIGN_PARAGRAPH.CENTER,
                   'right':WD_ALIGN_PARAGRAPH.RIGHT}[align]
    r = p.add_run(str(text)); r.bold = bold; r.font.size = Pt(size)
    if white: r.font.color.rgb = RGBColor(0xFF,0xFF,0xFF)
    elif color: r.font.color.rgb = color

def h1(text):
    p = doc.add_paragraph(); p.space_before = Pt(6)
    r = p.add_run(text); r.bold = True; r.font.size = Pt(15); r.font.color.rgb = BLUE
    return p

def h2(text):
    p = doc.add_paragraph()
    r = p.add_run(text); r.bold = True; r.font.size = Pt(12); r.font.color.rgb = BLUE2
    return p

def para(text, size=10.5, italic=False, color=None):
    p = doc.add_paragraph()
    r = p.add_run(text); r.font.size = Pt(size); r.italic = italic
    if color: r.font.color.rgb = color
    return p

def bullet(text, size=10.5):
    p = doc.add_paragraph(style='List Bullet')
    r = p.add_run(text); r.font.size = Pt(size)
    return p

def table(headers, rows, widths=None, total_row=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = 'Table Grid'
    for i, htext in enumerate(headers):
        c = t.rows[0].cells[i]; shade(c, HDR_FILL)
        set_cell(c, htext, bold=True, size=9.5, white=True,
                 align='left' if i == (1 if len(headers) > 3 else 0) else 'center')
    for ri, row in enumerate(rows):
        cells = t.add_row().cells
        for i, val in enumerate(row):
            if ri % 2 == 1: shade(cells[i], ALT_FILL)
            set_cell(cells[i], val, size=9,
                     align='left' if i == (1 if len(headers) > 3 else 0) else 'center')
    if total_row:
        cells = t.add_row().cells
        for i, val in enumerate(total_row):
            shade(cells[i], "DCE6F1")
            set_cell(cells[i], val, bold=True, size=9.5,
                     align='left' if i == (1 if len(headers) > 3 else 0) else 'center')
    if widths:
        for row in t.rows:
            for i, w in enumerate(widths):
                row.cells[i].width = Cm(w)
    return t


/**
 * Generate a full prompt by combining CORE + SPECIALIZATION and
 * substituting {{business_name}} with the actual business name.
 */
export function generatePrompt(templateId: string, businessName: string): string {
  const spec = SPECIALIZATIONS[templateId];
  if (!spec) return '';
  const raw = CORE + '\n\n' + spec.prompt;
  return raw.replace(/\{\{business_name\}\}/g, businessName);
}

/**
 * List of all templates for rendering in the UI.
 */
export const PROMPT_TEMPLATES = Object.entries(SPECIALIZATIONS).map(([id, s]) => ({
  id,
  name: s.name,
  icon: s.icon,
}));

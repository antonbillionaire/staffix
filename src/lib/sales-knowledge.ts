/**
 * Поиск релевантного топика по запросу.
 */
export function findSalesFact(query: string): { topicId: string; content: string } | null {
  const q = query.toLowerCase().trim();
  if (!q) return null;

  // Точное совпадение по id
  const byId = SALES_TOPICS.find((t) => t.id === q);
  if (byId) return { topicId: byId.id, content: byId.content };

  // Поиск по aliases
  for (const topic of SALES_TOPICS) {
    for (const alias of topic.aliases) {
      const a = alias.toLowerCase();
      if (q.includes(a) || a.includes(q)) {
        return { topicId: topic.id, content: topic.content };
      }
    }
  }

  return null;
}

export function listSalesTopicIds(): string[] {
  return SALES_TOPICS.map((t) => t.id);
}

/**
 * Калькулятор ROI для конкретного клиента.
 * Принимает цифры клиента, возвращает структурированный расчёт чтобы Виктор
 * мог их использовать в ответе.
 */
export interface RoiCalculation {
  monthlyLoss: number;
  staffixCost: number;
  roiMultiplier: number;
  formattedSummary: string;
  paybackInClients: number;
}

export function calculateRoi(
  avgCheck: number,
  lostClientsPerMonth: number,
  planPriceUsd: number = 45,
  currency: "USD" | "UZS" | "RUB" | "KZT" = "USD"
): RoiCalculation {
  const monthlyLoss = avgCheck * lostClientsPerMonth;

  // Конвертация плана в валюту клиента
  const fxRate: Record<string, number> = {
    USD: 1,
    UZS: 12500,
    RUB: 90,
    KZT: 480,
  };
  const staffixCost = Math.round(planPriceUsd * (fxRate[currency] || 1));

  const roiMultiplier = staffixCost > 0 ? +(monthlyLoss / staffixCost).toFixed(1) : 0;
  const paybackInClients = avgCheck > 0 ? Math.ceil(staffixCost / avgCheck) : 0;

  const formatted = (n: number) =>
    n.toLocaleString(currency === "UZS" ? "uz-UZ" : currency === "RUB" ? "ru-RU" : "en-US");

  const formattedSummary =
    `Потери в месяц: ${formatted(monthlyLoss)} ${currency} ` +
    `(${lostClientsPerMonth} клиентов × ${formatted(avgCheck)} ${currency}). ` +
    `Staffix: ${formatted(staffixCost)} ${currency}. ` +
    `ROI: ${roiMultiplier}× (окупается за ${paybackInClients} ${paybackInClients === 1 ? "клиента" : "клиентов"} в месяц).`;

  return {
    monthlyLoss,
    staffixCost,
    roiMultiplier,
    formattedSummary,
    paybackInClients,
  };
}

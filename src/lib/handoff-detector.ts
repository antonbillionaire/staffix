/**
 * Detector: бот в финальном тексте обещал передать запрос менеджеру / соединить с человеком?
 *
 * Используется в safety-net: если этот regex даёт truthy, а tool `notify_manager`
 * за оборот не вызывался — значит бот соврал, программно вызываем tool сами.
 *
 * Принцип: широкое покрытие. Лучше отправить владельцу одно лишнее уведомление
 * (с цитатой запроса клиента), чем потерять реальную эскалацию и сорвать продажу.
 *
 * Покрывает:
 *   1) Глаголы передачи + упоминание представителя: "передал/передам/передаю/передан…
 *      менеджеру/админу/оператору/сотруднику/специалисту/консультанту/человеку"
 *   2) Глаголы обработки заявки: "оставил заявку, передал запрос, оформил обращение,
 *      зарегистрировал заявку, заявка принята"
 *   3) Промисы контакта от человека: "менеджер свяжется/перезвонит/ответит/обработает/получит"
 *   4) Обещания обратного звонка: "перезвоним вам, свяжемся с вами, мы вернёмся к вам"
 *   5) English эквиваленты: forward to manager, notify manager, escalate, get back to you
 */

const HUMAN_REPS = "менедж|админ|оператор|сотрудник|специалист|консультант|представит|человек";

const FORWARDING_VERBS =
  "передал|передам|передаю|передан[оая]?|сообщил|сообщу|сообщаю|сообщим|" +
  "уведомил|уведомлю|уведомляю|уведомим|" +
  "оставил|оставлю|оставим|оставля?ю|" +
  "оформил|оформлю|оформляю|оформим|" +
  "отправил|отправлю|отправил.{0,3}запрос|отправил.{0,3}заявк|" +
  "зафикс|зарегистр|" +
  "попрошу|позову|привлек|связал.{0,3}с";

const REQUEST_NOUNS =
  "запрос|заявк|обращени|сообщени|заметку|пожелани";

const CONTACT_VERBS =
  "свяж[ёеу]т|увид[яеи]т|получит|перезвон|ответит|обработ|рассмотр|поможет";

// 1) "передал ... менеджеру" / "уведомил ... админа"
const FORWARDING_REPS_FORWARD = new RegExp(
  `(?:${FORWARDING_VERBS}).{0,60}(?:${HUMAN_REPS})`,
  "i"
);

// 2) "оставил заявку" / "передал запрос" — обещание оформления независимо от упоминания человека
const REQUEST_HANDLING = new RegExp(
  `(?:${FORWARDING_VERBS}).{0,30}(?:${REQUEST_NOUNS})`,
  "i"
);

// 3) "менеджер свяжется" / "оператор перезвонит" / "сотрудник свяжется"
const REP_CONTACT_BACK = new RegExp(
  `(?:${HUMAN_REPS}).{0,40}(?:${CONTACT_VERBS})`,
  "i"
);

// 4) "мы перезвоним" / "свяжемся с вами" / "обработаем ваш запрос"
const WE_CONTACT_YOU = /(перезвон[ия]м|свяжемся|обработаем|свяжемся\s+(с\s+)?вам|перезвоним\s+(вам|вас))/i;

// 5) Заявка как существительное в роли «принято»: "ваш запрос принят", "заявка зарегистрирована"
const REQUEST_RECEIVED = new RegExp(
  `(?:${REQUEST_NOUNS}).{0,30}(прин|зафикс|зарегистр|оформл|обработ)`,
  "i"
);

// 6) English
const ENGLISH = /(forward.{0,30}(manager|admin|representative|team)|notify.{0,15}(manager|admin|team)|escalat|get\s+back\s+to\s+you|sent\s+(to|the).{0,20}(manager|admin)|our\s+(team|manager).{0,20}(contact|reach|get)|will\s+contact\s+you)/i;

/**
 * Возвращает один объединённый regex — для использования с .test().
 * Любая из 6 групп срабатывает → trigger safety-net.
 */
export function botPromisedHandoffRegex(): RegExp {
  return new RegExp(
    [
      FORWARDING_REPS_FORWARD.source,
      REQUEST_HANDLING.source,
      REP_CONTACT_BACK.source,
      WE_CONTACT_YOU.source,
      REQUEST_RECEIVED.source,
      ENGLISH.source,
    ].join("|"),
    "i"
  );
}

/**
 * Удобная функция: вернёт true если нужно сработать safety-net.
 */
export function botPromisedHandoff(text: string): boolean {
  if (!text || text.length < 5) return false;
  return botPromisedHandoffRegex().test(text);
}

export type SupportChatRole = "user" | "assistant";

export interface SupportChatMessage {
  role: SupportChatRole;
  content: string;
}

/**
 * Генерирует ответ AI-помощника поддержки.
 * history — предыдущие сообщения диалога (без текущего сообщения пользователя).
 * userMessage — новое сообщение пользователя.
 */
export async function generateSupportReply(
  history: SupportChatMessage[],
  userMessage: string
): Promise<string> {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    return "Извините, сервис временно недоступен. Попробуйте позже или напишите на support@staffix.io.";
  }

  try {
    const anthropic = new Anthropic({ apiKey });
    const trimmed = history.slice(-20);
    const messages = [
      ...trimmed.map((m) => ({ role: m.role, content: m.content })),
      { role: "user" as const, content: userMessage },
    ];

    const response = await anthropic.messages.create({
      model: "claude-sonnet-4-5-20250929",
      max_tokens: 1024,
      system: SUPPORT_SYSTEM_PROMPT,
      messages,
    });

    const block = response.content[0];
    if (block && block.type === "text") return block.text;
    return "Извините, не смог обработать запрос. Попробуйте переформулировать.";
  } catch (error) {
    console.error("[support-prompt] generateSupportReply failed:", error);
    return "Произошла ошибка. Попробуйте позже или напишите на support@staffix.io.";
  }
}

/**
 * Проверяет, является ли ответ AI эскалацией к живому специалисту.
 * Используется чтобы автоматически уведомить администратора.
 */
export function isEscalationResponse(response: string): boolean {
  const lower = response.toLowerCase();
  return (
    lower.includes("передам ваше обращение") ||
    lower.includes("передать ваше обращение") ||
    lower.includes("свяжется с вами в ближайшее время") ||
    lower.includes("передам специалисту") ||
    lower.includes("передаю специалисту")
  );
}

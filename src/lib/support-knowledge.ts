/**
 * Поиск релевантного топика по запросу. Возвращает контент или null.
 * Простой substring/alias-матчинг — для базы из 16 топиков этого достаточно.
 */
export function findFeatureInfo(query: string): { topicId: string; content: string } | null {
  const q = query.toLowerCase().trim();
  if (!q) return null;

  // 1. Точное совпадение по id
  const byId = SUPPORT_TOPICS.find((t) => t.id === q);
  if (byId) return { topicId: byId.id, content: byId.content };

  // 2. Поиск по aliases (substring обоих направлений)
  for (const topic of SUPPORT_TOPICS) {
    for (const alias of topic.aliases) {
      const a = alias.toLowerCase();
      if (q.includes(a) || a.includes(q)) {
        return { topicId: topic.id, content: topic.content };
      }
    }
  }

  return null;
}

/** Список всех топиков для подсказки модели — какие вообще есть. */
export function listTopicIds(): string[] {
  return SUPPORT_TOPICS.map((t) => t.id);
}
